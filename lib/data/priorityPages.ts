/** Search Console で最優先確認すべきURL（インデックス登録・表示回数確認） */
export const SEARCH_CONSOLE_PRIORITY_URLS = PRIORITY_PAGES.map((p) => p.canonicalUrl);

/** primaryKeyword の重複チェック用（開発時確認用） */
export function checkKeywordDuplication(): void {
  const seen = new Map<string, string>();
  PRIORITY_PAGES.forEach((p) => {
    const existing = seen.get(p.primaryKeyword);
    if (existing) {
      console.warn(`[SEO] primaryKeyword重複: "${p.primaryKeyword}" → ${existing} と ${p.url}`);
    } else {
      seen.set(p.primaryKeyword, p.url);
    }
  });
}
